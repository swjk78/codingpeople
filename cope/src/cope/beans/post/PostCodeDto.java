package cope.beans.post;

public class PostCodeDto {
	private int codeNo;
	private int codePostNo;
	private String codeUrl;
	public int getCodeNo() {
		return codeNo;
	}
	public void setCodeNo(int codeNo) {
		this.codeNo = codeNo;
	}
	public int getCodePostNo() {
		return codePostNo;
	}
	public void setCodePostNo(int codePostNo) {
		this.codePostNo = codePostNo;
	}
	public String getCodeUrl() {
		return codeUrl;
	}
	public void setCodeUrl(String codeUrl) {
		this.codeUrl = codeUrl;
	}
	public PostCodeDto() {
		super();
	}
	
	
}
